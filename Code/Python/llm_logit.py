@torch.no_grad()
def calculate_token_probabilities_only(model, tokenizer, dialogue_info, utterance_id_for_output, reasoning_text, model_id="unknown"):
    """Calculate token probabilities using existing reasoning text"""
    
    # Step 1: Generate classification using existing reasoning
    classification_messages = make_classification_prompt_messages(dialogue_info, utterance_id_for_output, reasoning_text)
    
    try:
        classification_prompt_for_model = tokenizer.apply_chat_template(
            classification_messages, tokenize=False, add_generation_prompt=True, enable_thinking=False
        )
        # No additional FINAL_CLASSIFICATION needed since it's already in the user prompt

    except Exception as e:
        print(f"Warning: tokenizer.apply_chat_template failed for classification: {e}. Using manual concatenation.")
        # Similar fallback logic as reasoning step
        classification_prompt_for_model = ""
        system_content = ""
        user_content = ""

        for msg in classification_messages:
            if msg['role'] == 'system':
                system_content = msg['content']
            elif msg['role'] == 'user':
                user_content = msg['content']

        combined_user_content = user_content
        if system_content:
            combined_user_content = f"SYSTEM INSTRUCTIONS:\n{system_content}\n\nUSER TASK:\n{user_content}"

        if "gemma" in model_id.lower():
            classification_prompt_for_model = tokenizer.bos_token if tokenizer.bos_token else ""
            classification_prompt_for_model += f"<start_of_turn>user\n{combined_user_content}<end_of_turn>\n"
            classification_prompt_for_model += f"<start_of_turn>model\n"
        elif "qwen" in model_id.lower():
            classification_prompt_for_model = f"<|im_start|>system\n{system_content}<|im_end|>\n" if system_content else ""
            classification_prompt_for_model += f"<|im_start|>user\n{user_content}<|im_end|>\n<|im_start|>assistant\n"
        elif "llama-3" in model_id.lower():
            classification_prompt_for_model = tokenizer.bos_token if tokenizer.bos_token else ""
            for msg in classification_messages:
                start_header = getattr(tokenizer, 'special_tokens_map', {}).get('<|start_header_id|>', '<|start_header_id|>')
                end_header = getattr(tokenizer, 'special_tokens_map', {}).get('<|end_header_id|>', '<|end_header_id|>')
                eot = getattr(tokenizer, 'special_tokens_map', {}).get('<|eot_id|>', '<|eot_id|>')
                classification_prompt_for_model += f"{start_header}{msg['role']}{end_header}\n\n{msg['content']}{eot}"
            classification_prompt_for_model += f"{start_header}assistant{end_header}\n\n"
        elif "phi" in model_id.lower():
            classification_prompt_for_model = f"<|user|>\n{combined_user_content}<|end|>\n<|assistant|>\n"
        else:
            classification_prompt_for_model = f"System: {system_content}\n" if system_content else ""
            classification_prompt_for_model += f"User: {user_content}\nAssistant:\n"

    # Prepare classification inputs for logit calculation only
    model_max_len = tokenizer.model_max_length or 8192
    context_len_budget = max(64, model_max_len - 100)  # No need for generation budget
    classification_inputs = tokenizer(classification_prompt_for_model, return_tensors="pt", padding="longest", truncation=True, max_length=context_len_budget)
    
    # No generation needed - just calculate probabilities directly
    # Create a dummy classification line for consistency
    current_id_str = str(utterance_id_for_output).strip()
    speaker_name = dialogue_info.get('current_speaker', 'Speaker')
    final_output_line = f"{current_id_str} : {speaker_name} : LOGIT_CALCULATION_ONLY : NA"
    
    print(f"Calculating logits for ID {utterance_id_for_output} using existing reasoning (no generation needed)")

    classification_labels = [
        "Explain Own Idea",
        "Engage Others Idea (LOW)",
        "Engage Others Idea (MEDIUM)", 
        "Engage Others Idea (HIGH)",
        "Uncorrelated"
    ]
    
    speaker_name = dialogue_info.get('current_speaker', 'Speaker')
        
    reference_for_logits = "Peer"  
    try:
        parts_for_ref = final_output_line.split(' : ')
        if len(parts_for_ref) >= 4:
            predicted_ref = parts_for_ref[3].strip()
            if predicted_ref and predicted_ref.upper() != "NA":
                reference_for_logits = predicted_ref
    except Exception as e:
        print(f"DEBUG: Failed to parse reference from classification line: {e}")

    if reference_for_logits == "Peer":
        if 'previous_speaker_1' in dialogue_info and dialogue_info.get('previous_speaker_1') and dialogue_info['previous_speaker_1'] != speaker_name:
            reference_for_logits = dialogue_info['previous_speaker_1']

    print(f"DEBUG: Using reference '{reference_for_logits}' for all EOI logit calculations")
    
    possible_classifications = {}
    current_id_str = str(utterance_id_for_output).strip()

    for label in classification_labels:
        if "Engage Others Idea" in label:
            classification_line = f" {current_id_str} : {speaker_name} : {label} : {reference_for_logits}"
        else:
            classification_line = f" {current_id_str} : {speaker_name} : {label} : NA"
        
        possible_classifications[label] = classification_line

    # Calculate log probabilities
    classification_logits = {}
    with torch.no_grad():
        for label, classification_line in possible_classifications.items():
            full_sequence = classification_prompt_for_model + classification_line
            try:
                full_inputs = tokenizer(full_sequence, return_tensors="pt", truncation=True, max_length=context_len_budget)
                outputs = model(
                    input_ids=full_inputs["input_ids"].to(model.device),
                    attention_mask=full_inputs["attention_mask"].to(model.device)
                )
                prompt_length = classification_inputs["input_ids"].shape[1]
                classification_token_ids = full_inputs["input_ids"][0, prompt_length:]
                
                if len(classification_token_ids) > 0:
                    classification_logits_tensor = outputs.logits[0, prompt_length-1:prompt_length-1+len(classification_token_ids), :]
                    log_probs = torch.log_softmax(classification_logits_tensor, dim=-1)
                    total_log_prob = sum(log_probs[i, token_id].item() for i, token_id in enumerate(classification_token_ids) if i < log_probs.shape[0])
                    classification_logits[label] = total_log_prob
                    print(f"DEBUG: Label '{label}' -> sequence log_prob: {total_log_prob:.4f}")
                else:
                    classification_logits[label] = float('-inf')
                    print(f"DEBUG: Label '{label}' -> no tokens to evaluate")
            except Exception as e:
                print(f"  [WARN] Error calculating sequence probability for {label}: {e}")
                classification_logits[label] = float('-inf')
    
    print(f"DEBUG: Raw sequence-based logits: {classification_logits}")
    
    # Softmax normalization
    valid_logits = [(k, v) for k, v in classification_logits.items() if v != float('-inf')]
    if valid_logits:
        logit_values = [v for k, v in valid_logits]
        logit_tensor = torch.tensor(logit_values)
        log_probs_normalized = torch.log_softmax(logit_tensor, dim=0)
        probs = torch.softmax(logit_tensor, dim=0)
        
        print("DEBUG: Normalized sequence-based logits (CSV column mapping):")
        for i, (label, _) in enumerate(valid_logits):
            classification_logits[label] = log_probs_normalized[i].item()
            # Map to CSV column names
            if label == 'Explain Own Idea':
                csv_col = 'logit_explain_own_idea'
            elif label == 'Engage Others Idea (LOW)':
                csv_col = 'logit_engage_others_low'
            elif label == 'Engage Others Idea (MEDIUM)':
                csv_col = 'logit_engage_others_medium'
            elif label == 'Engage Others Idea (HIGH)':
                csv_col = 'logit_engage_others_high'
            elif label == 'Uncorrelated':
                csv_col = 'logit_uncorrelated'
            else:
                csv_col = 'unknown'
            print(f"  '{label}' -> log_prob: {log_probs_normalized[i].item():.4f}, prob: {probs[i].item():.4f} -> {csv_col}")
        
        for label in classification_labels:
            if label not in dict(valid_logits):
                classification_logits[label] = -1000.0
    
    try:        
        classification_prefix = classification_prompt_for_model + f" {current_id_str} : {speaker_name} :"
        prefix_inputs = tokenizer(classification_prefix, return_tensors="pt", truncation=True, max_length=context_len_budget)
        with torch.no_grad():
            outputs_prefix = model(
                input_ids=prefix_inputs["input_ids"].to(model.device),
                attention_mask=prefix_inputs["attention_mask"].to(model.device),
                use_cache=False 
            )
        logits_next = outputs_prefix.logits[0, -1]
        probs_next = torch.softmax(logits_next, dim=-1)

        label_first_tokens = {
            "Explain Own Idea": " Explain",
            "Engage Others Idea": " Engage",
            "Uncorrelated": " Uncorrelated"
        }
        for lbl, tok_txt in label_first_tokens.items():
            tok_ids = tokenizer.encode(tok_txt, add_special_tokens=False)
            if tok_ids:
                classification_logits[f"token_prob_{lbl.lower().replace(' ', '_').replace('(', '').replace(')', '')}"] = probs_next[tok_ids[0]].item()

        engage_prefix = classification_prefix + " Engage Others Idea ("
        engage_inputs = tokenizer(engage_prefix, return_tensors="pt", truncation=True, max_length=context_len_budget)
        with torch.no_grad():
            outputs_engage = model(
                input_ids=engage_inputs["input_ids"].to(model.device),
                attention_mask=engage_inputs["attention_mask"].to(model.device),
                use_cache=False
            )
        logits_engage_next = outputs_engage.logits[0, -1]
        probs_engage_next = torch.softmax(logits_engage_next, dim=-1)
        for lvl in ["LOW", "MEDIUM", "HIGH"]:
            tok_id_lvl = tokenizer.encode(lvl, add_special_tokens=False)
            if tok_id_lvl:
                classification_logits[f"token_prob_engage_{lvl.lower()}"] = probs_engage_next[tok_id_lvl[0]].item()
        
        print("DEBUG next-token probs (CSV column mapping):")
        print(f"  EXP:{classification_logits.get('token_prob_explain_own_idea', float('nan')):.4f} -> tokprob_explain_own_idea")
        print(f"  EOI:{classification_logits.get('token_prob_engage_others_idea', float('nan')):.4f} -> tokprob_engage_others")
        print(f"  UC:{classification_logits.get('token_prob_uncorrelated', float('nan')):.4f} -> tokprob_uncorrelated")
        print(f"  EOI-L:{classification_logits.get('token_prob_engage_low', float('nan')):.4f} -> tokprob_engage_low")
        print(f"  EOI-M:{classification_logits.get('token_prob_engage_medium', float('nan')):.4f} -> tokprob_engage_medium")
        print(f"  EOI-H:{classification_logits.get('token_prob_engage_high', float('nan')):.4f} -> tokprob_engage_high")
    except Exception as e:
        print(f"[WARN] Next-token label prob calculation failed: {e}")
    
    return final_output_line, reasoning_text, classification_logits



# --- 5. main function ---
def process_dialogue_dataframe_with_existing_reasoning(df_with_reasoning, model, tokenizer, model_id="unknown"):
    """Process dialogue dataframe using existing reasoning from CSV file"""
    results = []
    
    # Sort by group, date, and block_index to ensure proper ordering
    # df_sorted = df_with_reasoning.sort_values(['group', 'date', 'block_index', 'utterance_id']).reset_index(drop=True)
    df_sorted = df_with_reasoning
    for index, row in tqdm(df_sorted.iterrows(), total=len(df_sorted), desc="Processing Token Probabilities"):
        current_utterance_id = str(row['utterance_id']) 
        current_group = str(row.get('group', "UnknownGroup"))
        current_date_str = str(row.get('date', "UnknownDate")) 
        current_timestamp_str = str(row.get('timestamp', "N/A"))
        current_block_index = row.get('block_index', 1)
        
        current_speaker = str(row.get('name', "UnknownSpeaker"))
        current_utterance_text = str(row.get('utterance', ""))
        existing_reasoning = clean_reasoning_text(str(row.get('llm_reasoning', "")))
        
        # Build dialogue context with previous utterances from same group/date/block
        dialogue_info = {
            "current_block_index": current_block_index,
            "current_group": current_group,
            "current_date": current_date_str, 
            "current_speaker": current_speaker,
            "current_utterance_text": current_utterance_text,
            "current_utterance_indent": 0
        }
        
        # Find previous utterances in the same group, date, and block
        same_context_mask = (
            (df_sorted['group'] == current_group) & 
            (df_sorted['date'] == current_date_str) & 
            (df_sorted['block_index'] == current_block_index) &
            (df_sorted.index < index)  # Only consider previous utterances
        )
        previous_utterances = df_sorted[same_context_mask].tail(3)  # Get up to 3 previous utterances
        
        if len(previous_utterances) >= 1:
            prev_1 = previous_utterances.iloc[-1]
            dialogue_info["previous_utterance_1"] = str(prev_1.get('utterance', 'N/A'))
            dialogue_info["previous_speaker_1"] = str(prev_1.get('name', 'N/A'))
        else:
            dialogue_info["previous_utterance_1"] = "N/A"
            dialogue_info["previous_speaker_1"] = "N/A"
            
        if len(previous_utterances) >= 2:
            prev_2 = previous_utterances.iloc[-2]
            dialogue_info["previous_utterance_2"] = str(prev_2.get('utterance', 'N/A'))
            dialogue_info["previous_speaker_2"] = str(prev_2.get('name', 'N/A'))
        else:
            dialogue_info["previous_utterance_2"] = "N/A"
            dialogue_info["previous_speaker_2"] = "N/A"
            
        if len(previous_utterances) >= 3:
            prev_3 = previous_utterances.iloc[-3]
            dialogue_info["previous_utterance_3"] = str(prev_3.get('utterance', 'N/A'))
            dialogue_info["previous_speaker_3"] = str(prev_3.get('name', 'N/A'))
        else:
            dialogue_info["previous_utterance_3"] = "N/A"
            dialogue_info["previous_speaker_3"] = "N/A"
        
        classification_result_line = f"{current_utterance_id} : {current_speaker} : CLASSIFICATION_FAILED : NA"
        reasoning_text = existing_reasoning
        classification_logits = {}
        
        if not current_utterance_text.strip() or not existing_reasoning.strip():
            classification_result_line = f"{current_utterance_id} : {current_speaker} : Uncorrelated : NA"
            reasoning_text = existing_reasoning if existing_reasoning.strip() else "Empty utterance or reasoning"
            # Set default logits for empty utterance
            classification_logits = {
                "Explain Own Idea": float('-inf'),
                "Engage Others Idea (LOW)": float('-inf'),
                "Engage Others Idea (MEDIUM)": float('-inf'),
                "Engage Others Idea (HIGH)": float('-inf'),
                "Uncorrelated": 0.0  # Give highest logit to Uncorrelated for empty utterances
            }
        else:
            try:
                classification_result_line, reasoning_text, classification_logits = calculate_token_probabilities_only(
                    model, tokenizer, dialogue_info, current_utterance_id, existing_reasoning, model_id=model_id
                )
            except Exception as e:
                print(f"  [CRITICAL ERROR] Token probability calculation failed for utterance ID {current_utterance_id}: '{current_utterance_text[:50]}...' - {e}")
                import traceback; traceback.print_exc()
                reasoning_text = existing_reasoning
                # Set default error logits
                classification_logits = {
                    "Explain Own Idea": float('-inf'),
                    "Engage Others Idea (LOW)": float('-inf'),
                    "Engage Others Idea (MEDIUM)": float('-inf'),
                    "Engage Others Idea (HIGH)": float('-inf'),
                    "Uncorrelated": float('-inf')
                }
        
        # Copy existing data and add new logit information
        result_row = row.to_dict()
        
        # Remove unnecessary parsed_* columns if they exist
        columns_to_remove = [col for col in result_row.keys() if col.startswith('parsed_')]
        for col in columns_to_remove:
            result_row.pop(col, None)
        
        result_row.update({
            'llm_classification_output_line_new': classification_result_line,
            'logit_explain_own_idea': classification_logits.get('Explain Own Idea', float('-inf')),
            'logit_engage_others_low': classification_logits.get('Engage Others Idea (LOW)', float('-inf')),
            'logit_engage_others_medium': classification_logits.get('Engage Others Idea (MEDIUM)', float('-inf')),
            'logit_engage_others_high': classification_logits.get('Engage Others Idea (HIGH)', float('-inf')),
            'logit_uncorrelated': classification_logits.get('Uncorrelated', float('-inf')),
            # --- next-token probs ---
            'tokprob_explain_own_idea': classification_logits.get('token_prob_explain_own_idea', np.nan),
            'tokprob_engage_others': classification_logits.get('token_prob_engage_others_idea', np.nan),
            'tokprob_uncorrelated': classification_logits.get('token_prob_uncorrelated', np.nan),
            'tokprob_engage_low': classification_logits.get('token_prob_engage_low', np.nan),
            'tokprob_engage_medium': classification_logits.get('token_prob_engage_medium', np.nan),
            'tokprob_engage_high': classification_logits.get('token_prob_engage_high', np.nan)
        })
        
        results.append(result_row)
            
        if (index + 1) % 20 == 0: 
            if torch.cuda.is_available():
                torch.cuda.empty_cache()
    
    if torch.cuda.is_available(): 
        torch.cuda.empty_cache()

    return pd.DataFrame(results)

# --- 6. LLM output parsing ---
def parse_llm_output_line(line_str, expected_id_str):
    parts = line_str.split(' : ')
    if len(parts) == 4:
        parsed_id = parts[0].strip()
        parsed_name = parts[1].strip()
        llm_label_full = parts[2].strip() 
        llm_reference = parts[3].strip()

        llm_label_main = llm_label_full
        llm_engagement = "NA" 

        if "Engage Others Idea" in llm_label_full:
            if "(LOW)" in llm_label_full:
                llm_label_main = "Engage Others Idea"
                llm_engagement = "LOW"
            elif "(MEDIUM)" in llm_label_full:
                llm_label_main = "Engage Others Idea"
                llm_engagement = "MEDIUM"
            elif "(HIGH)" in llm_label_full:
                llm_label_main = "Engage Others Idea"
                llm_engagement = "HIGH"

        result_dict = {
            "parsed_utterance_id": parsed_id,
            "parsed_name": parsed_name,
            "parsed_indent": "0",  # Default value since it's no longer in the input
            "llm_label": llm_label_main,      
            "llm_engagement": llm_engagement, 
            "llm_reference": llm_reference
        }

        if parsed_id == expected_id_str:
            return result_dict
        else:
            print(f"  [PARSE_ID_MISMATCH] Expected ID {expected_id_str} but got {parsed_id} in line: '{line_str}'")
            result_dict["llm_label"] = f"ID_MISMATCH_{llm_label_main}"
            return result_dict

    print(f"  [PARSE_WARN] LLM output line does not have 4 parts. Line: '{line_str}' for ID {expected_id_str} (Found {len(parts)} parts)")
    return {
        "parsed_utterance_id": str(expected_id_str), 
        "parsed_name": "ERR_FORMAT",
        "parsed_indent": "0",  # Default value
        "llm_label": "LINE_FORMAT_ERROR", 
        "llm_engagement": "NA",
        "llm_reference": "NA"
    }

