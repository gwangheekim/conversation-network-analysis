def debug_next_token_probs(model, tokenizer, prefix_text, top_k=20):
    """Utility: Print top-k next-token probabilities given a prefix.

    Parameters
    ----------
    model, tokenizer : The loaded HF model & tokenizer (same dtype/device as main code)
    prefix_text : str
        Full prompt string fed to the model *up to* the position where you want to inspect logits.
    top_k : int, optional
        Number of most-probable tokens to display (default 20).
    """
    model.eval()
    with torch.no_grad():
        inputs = tokenizer(prefix_text, return_tensors="pt")
        inputs = {k: v.to(model.device) for k, v in inputs.items()}
        outputs = model(**inputs)
        # Take logits of the last position (the next-token distribution)
        logits = outputs.logits[0, -1]
        probs = torch.softmax(logits, dim=-1)
        topk_probs, topk_indices = torch.topk(probs, top_k)
        topk_tokens = tokenizer.convert_ids_to_tokens(topk_indices.tolist())
        print(f"Top-{top_k} tokens after prefix (probabilities):")
        for tok, p in zip(topk_tokens, topk_probs):
            tok_clean = tok.replace('\u0120', '▁')  # visualise sentencepiece spaces if any
            print(f"{tok_clean:\<15} : {p.item():.5f}")
        return list(zip(topk_tokens, topk_probs.tolist()))


